package typecastingdemo;

public class typecastingdemo1 {
	public static void main(String[] args) {
		
		int x = 353;
		double y =  x;
		double z= 133;
		int i = (int)z;
		
		System.out.println(x);
		System.out.println(y);
		
		System.out.println(z);
		System.out.println(i);
		
	}

}
